#!/usr/bin/env node
/**
 * Batch process NEV folders
 * Process each folder one by one via Marcus-EV
 */

const fs = require('fs');
const path = require('path');
const { spawn } = require('child_process');

const UPLOAD_DIR = '/Users/imodteam/Desktop/NEV-Database/Upload';
const ACTIVITY_FILE = path.join(UPLOAD_DIR, 'activity.json');
const VERSION_FILE = '/Users/imodteam/projects/imod-portal/NEV_VERSION.txt';
const CHANGELOG_FILE = '/Users/imodteam/projects/imod-portal/NEV_CHANGELOG.md';

let processed = 0;
let failed = 0;
let total = 0;

async function processBatch() {
  console.log('🚀 Starting batch processing...\n');
  
  // Read activity log
  const activities = JSON.parse(fs.readFileSync(ACTIVITY_FILE, 'utf8'));
  
  // Filter pending
  const pending = activities.filter(a => a.status === 'pending_parse');
  total = pending.length;
  
  console.log(`📊 Found ${total} folders to process\n`);
  
  if (total === 0) {
    console.log('✅ All folders processed!');
    return;
  }
  
  // Process one by one
  for (let i = 0; i < pending.length; i++) {
    const activity = pending[i];
    console.log(`\n[${ i + 1}/${total}] Processing: ${activity.folder}`);
    console.log(`Brand: ${activity.brand || 'Unknown'}`);
    console.log(`Model: ${activity.model || 'Unknown'}`);
    console.log(`Files: ${activity.files}\n`);
    
    try {
      await processFolderWithMarcus(activity);
      
      // Update status
      const idx = activities.findIndex(a => a.folder === activity.folder);
      if (idx !== -1) {
        activities[idx].status = 'completed';
        activities[idx].processedAt = new Date().toISOString();
      }
      
      // Save
      fs.writeFileSync(ACTIVITY_FILE, JSON.stringify(activities, null, 2));
      
      processed++;
      console.log(`✅ [${i + 1}/${total}] Complete!\n`);
      
      // Update version
      updateVersion(processed, total);
      
    } catch (err) {
      console.error(`❌ [${i + 1}/${total}] Failed:`, err.message);
      
      // Update status
      const idx = activities.findIndex(a => a.folder === activity.folder);
      if (idx !== -1) {
        activities[idx].status = 'error';
        activities[idx].error = err.message;
      }
      
      fs.writeFileSync(ACTIVITY_FILE, JSON.stringify(activities, null, 2));
      failed++;
    }
    
    // Small delay between folders
    await sleep(2000);
  }
  
  console.log('\n' + '='.repeat(60));
  console.log('🎉 Batch processing complete!');
  console.log(`✅ Processed: ${processed}/${total}`);
  console.log(`❌ Failed: ${failed}/${total}`);
  console.log('='.repeat(60) + '\n');
}

async function processFolderWithMarcus(activity) {
  const folderPath = path.join(UPLOAD_DIR, activity.folder);
  
  // Build task for Marcus-EV
  const task = `วิเคราะห์และนำเข้าข้อมูลรถยนต์ไฟฟ้า

**โฟลเดอร์:** ${activity.folder}
**Path:** ${folderPath}

**ข้อมูลเบื้องต้น:**
- แบรนด์: ${activity.brand || 'ระบุเอง'}
- รุ่น: ${activity.model || 'ระบุเอง'}
- จำนวนไฟล์: ${activity.files}

**งานที่ต้องทำ:**

1. **อ่านไฟล์ทั้งหมด** (PDF, DOCX, images)
2. **Extract specs ตาม 11 categories:**
   - Multimedia, Safety, Interior, Exterior
   - EV Features, Battery, Suspension, Brakes
   - Wheels & Tires, Powertrain, Dimensions
3. **สร้าง merged.json** บันทึกในโฟลเดอร์เดียวกัน
4. **นำเข้า NEV Database** ผ่าน Prisma
5. **แจ้งผลที่ #basement** พร้อมสรุป

**คำแนะนำ:**
- ถ้าไม่มีข้อมูลบาง field → ใส่ null
- ถ้ามีหลายรุ่นย่อย → สร้างทุกรุ่น
- ตรวจสอบว่ามีข้อมูลซ้ำหรือไม่ก่อน import

**รายงานผลให้ครบ!**`;

  console.log('📤 Sending task to Marcus-EV...');
  
  // Use sessions_spawn via OpenClaw
  return new Promise((resolve, reject) => {
    // Write task to temp file
    const taskFile = `/tmp/marcus-task-${Date.now()}.txt`;
    fs.writeFileSync(taskFile, task);
    
    const proc = spawn('openclaw', [
      'sessions', 'spawn',
      '--runtime', 'subagent',
      '--agent-id', 'marcus-ev',
      '--mode', 'run',
      '--task', task,
      '--run-timeout-seconds', '600'
    ]);
    
    let output = '';
    proc.stdout.on('data', data => {
      output += data.toString();
      process.stdout.write(data);
    });
    
    proc.stderr.on('data', data => {
      process.stderr.write(data);
    });
    
    proc.on('close', code => {
      fs.unlinkSync(taskFile);
      
      if (code === 0) {
        resolve(output);
      } else {
        reject(new Error(`Marcus-EV exited with code ${code}`));
      }
    });
  });
}

function updateVersion(current, total) {
  const version = `2.0.0-import-${current}-of-${total}`;
  
  fs.writeFileSync(VERSION_FILE, version);
  
  // Update changelog
  const changelogEntry = `\n## ${version} (${new Date().toISOString().split('T')[0]})\n\n- Imported ${current}/${total} folders\n- Auto-generated by batch processor\n`;
  
  if (fs.existsSync(CHANGELOG_FILE)) {
    const existing = fs.readFileSync(CHANGELOG_FILE, 'utf8');
    fs.writeFileSync(CHANGELOG_FILE, changelogEntry + existing);
  } else {
    fs.writeFileSync(CHANGELOG_FILE, `# NEV Database Changelog\n${changelogEntry}`);
  }
  
  console.log(`📝 Version updated: ${version}`);
}

function sleep(ms) {
  return new Promise(resolve => setTimeout(resolve, ms));
}

// Run
processBatch().catch(err => {
  console.error('Fatal error:', err);
  process.exit(1);
});
